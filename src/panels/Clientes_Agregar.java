    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (f.AddClient(nombre, direccion, telefono, rfc, mail))
            {
                f.JtextField_SetEmpty(nombre);
                f.JtextField_SetEmpty(direccion);
                f.JtextField_SetEmpty(telefono);
                f.JtextField_SetEmpty(mail);
                f.JtextField_SetEmpty(rfc);
                f.Alert("Cliente agregado");
            }else
            {
                f.Alert("Cliente NO agregado");
            }
        } catch (ClassNotFoundException | InstantiationException | IllegalAccessException | SQLException ex) {
            f.Alert(ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed


