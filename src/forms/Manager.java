    public void back ()
    {
        if (Funciones.idBack == Funciones.BackVehiculos_consultar)
        {
            ShowVehiculos_Consultar();
        }
        else if (Funciones.idBack == Funciones.BackVehiculos_agregar)
        {
            ShowVehiculos_Agregar();
        }
        else if (Funciones.idBack == Funciones.BackClientes_agregar)
        {
            ShowClientes_Agregar();
        }
        else if (Funciones.idBack == Funciones.BackClientes_consultar)
        {
            ShowClientes_Consultar();
        }
        else if (Funciones.idBack == Funciones.BackProvedores_Agregar)
        {
            ShowProvedores_Agregar();
        }
        else if (Funciones.idBack == Funciones.BackProvedores_Consultar)
        {
            ShowProvedores_Consultar();
        }
        else if (Funciones.idBack == Funciones.BackStock_Agregar)
        {
            ShowStock_Agregar();
        }
        else if (Funciones.idBack == Funciones.BackStock_Consultar)
        {
            ShowStock_Consultar();
        }
    }
    
    private void ClearDesktop ()
    {
        Desktop.removeAll();
        Desktop.repaint();
    }
    
    public void ShowVehiculos_Consultar()
    {
        if (f.Get_Permiso(Funciones.PermisoVehiculos))
        {
            Funciones.idBack = tmp;
            ClearDesktop();
            Vehiculos_Consultar form = new Vehiculos_Consultar();
            Desktop.add(form);
            form.setSize(Desktop.getWidth(), Desktop.getHeight());
            form.show();
            tmp = Funciones.BackVehiculos_consultar;
        }
    }
    
